package nl.saxion.re.solarsmart;

public class Customer {

    private String firstName;
    private String lastName;
    private double roofWidth;
    private double roofHeight;
    private int powerConsumption;

    public Customer (String firstName, String lastName, double roofWidth, double roofHeight, int powerConsumption) {

        this.firstName = firstName;
        this.lastName = lastName;
        this.roofWidth = roofWidth;
        this.roofHeight = roofHeight;
        this.powerConsumption = powerConsumption;

    }




}
